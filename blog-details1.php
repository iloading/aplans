										<p>A grande diversidade de castas existentes no Douro, adaptáveis a diferentes situações de clima, demonstra as condições ótimas para a cultura da vinha existentes na região. Nas castas tintas destacam-se a Tinta Amarela, Tinta Barroca, Tinta Roriz, Touriga Francesa, Touriga Nacional e Tinto Cão. As castas brancas predominantes são a Malvasia Fina, Viosinho, Donzelinho e Gouveio. No que diz respeito à produtividade, a região não se caracteriza por ter castas muito produtivas. Será de referir que o rendimento máximo permitido é de 55 hl/ha (cerca de 7.5OOkg/ha). A produtividade média é de cerca de 30 hl/ha (4. 100kg/ha).</p>


